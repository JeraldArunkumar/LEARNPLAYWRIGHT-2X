//Arithmetic operator will perform mathematical operations on numbers
let a = 10,b=5;

console.log(a + b); // 15 (addition)
console.log(a - b); // 5 (subtraction)
console.log(a * b); // 50 (multiplication)
console.log(a / b); // 2 (division)
console.log(a % b); // 0 (modulus - remainder of division)
console.log(a ** b); // 100000 (exponentiation - a raised to the power of b)    
