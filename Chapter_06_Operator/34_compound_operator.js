 //Compound operators are shorthand operators that combine an arithmetic or assignment operation with an assignment  
let x = 5;
x += 3; // This is equivalent to x = x + 3
console.log(x); // 8
x -= 2; // This is equivalent to x = x - 2
console.log(x); // 6
x *= 4; // This is equivalent to x = x * 4
console.log(x); // 24
x /= 6; // This is equivalent to x = x / 6
console.log(x); // 4
x %= 3; // This is equivalent to x = x % 3
console.log(x);
