//Modulus operator will return the remainder of a division operation
let a = 10, b = 3;
console.log(a % b); // 1 (10 divided by 3 is 3 with a remainder of 1)   
console.log(40 % 7); // 5 (40 divided by 7 is 5 with a remainder of 5)
console.log(15 % 4); // 3 (15 divided by 4 is 3 with a remainder of 3)
