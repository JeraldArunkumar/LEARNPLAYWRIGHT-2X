//objects are collections of key-value pairs. They are used to store and organize data in JavaScript.

//Creating an empty object
let obj={};
console.log(obj) //{}
//Creating an object with properties
let person={
    name: "Alice",
    age: 30
};
console.log(person); // { name: "Alice", age: 30 }

//creating objects in single line
let car={make: "Toyota", model: "Camry", year: 2020};
console.log(car) //{ make: "Toyota", model: "Camry", year: 2020 }