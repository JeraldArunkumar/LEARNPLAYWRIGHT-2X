//For loop helps to execute a block of code a specified number of times. It consists of three parts: initialization, condition, and increment/decrement. The loop continues to execute as long as the condition is true.
//The syntax of a for loop is as follows:
//for (initialization; condition; increment/decrement) {
//    // code to be executed
//}
for (let i = 0; i < 5; i++) {
    console.log(i); // This will print numbers from 0 to 4
}